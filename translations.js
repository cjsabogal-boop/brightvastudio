// SISTEMA DE TRADUCCIÓN CON ATRIBUTOS data-i18n
// ============================================

class TranslationManager {
    constructor() {
        this.currentLang = this.getSavedLanguage() || 'en';
        this.translations = translations;
    }

    // Guardar idioma en localStorage
    saveLanguage(lang) {
        localStorage.setItem('preferredLanguage', lang);
    }

    // Obtener idioma guardado
    getSavedLanguage() {
        return localStorage.getItem('preferredLanguage');
    }

    // Obtener valor anidado del objeto de traducciones
    getNestedValue(obj, path) {
        return path.split('.').reduce((current, key) => current?.[key], obj);
    }

    // Traducir todos los elementos con data-i18n
    translateAll(lang) {
        this.currentLang = lang;
        this.saveLanguage(lang);
        const t = this.translations[lang];

        // Traducir todos los elementos con data-i18n
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            const translation = this.getNestedValue(t, key);

            if (translation !== undefined) {
                // Si el elemento tiene data-i18n-html, usar innerHTML
                if (element.hasAttribute('data-i18n-html')) {
                    element.innerHTML = translation;
                } else {
                    element.textContent = translation;
                }
            }
        });

        // Reinicializar iconos de Lucide
        if (typeof lucide !== 'undefined') {
            lucide.createIcons();
        }

        // Actualizar botones de idioma
        this.updateLanguageButtons(lang);
    }

    // Actualizar estado visual de los botones de idioma
    updateLanguageButtons(lang) {
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.remove('active');
            if (btn.getAttribute('data-lang') === lang) {
                btn.classList.add('active');
            }
        });
    }

    // Inicializar el sistema de traducción
    init() {
        // Traducir a idioma guardado o inglés
        this.translateAll(this.currentLang);

        // Agregar event listeners a los botones de idioma
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.addEventListener('click', () => {
                const lang = btn.getAttribute('data-lang');
                this.translateAll(lang);
            });
        });
    }
}

// Exportar para uso global
if (typeof module !== 'undefined' && module.exports) {
    module.exports = { TranslationManager, translations };
}
